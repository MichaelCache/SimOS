static void print_font(int x,int y,char font[])
{
        unsigned char *vram=(unsigned char *)0x000a0000;
        int i,j,start;
        start=y*320+x;
	for(i=0;i<16;i++)
	{
		j=start+320*i;
		if((font[i]&0x80)!=0)
		{
			*(vram+j)=0;
		}
		if((font[i]&0x40)!=0)
		{
			*(vram+j+1)=0;
		}
		if((font[i]&0x20)!=0)
		{
			*(vram+j+2)=0;
		}
		if((font[i]&0x10)!=0)
		{
			*(vram+j+3)=0;
		}
		if((font[i]&0x08)!=0)
		{
			*(vram+j+4)=0;
		}
		if((font[i]&0x04)!=0)
		{
			*(vram+j+5)=0;
		}
		if((font[i]&0x02)!=0)
		{
			*(vram+j+6)=0;
		}
		if((font[i]&0x01)!=0)
		{
			*(vram+j+7)=0;
		}
	}
}

static void print_mouse(int x,int y)
{
        unsigned char *vram=(unsigned char *)0x000a0000;
        int i,j,start,next;
	char *p;
	p=(char *)mouse;
        start=y*320+x;
        for(i=0;i<16;i++)
	{
		next=start+320*i;
		for(j=0;j<16;j++)
		{
			if(*p=='2')
			{
				*(vram+j+next)=255;
			}
			if(*p=='1')
			{
				*(vram+j+next)=0;
			}
			p++;
		}
	}
}


